public class Comments {
   public static void main(String[] args) {
      System.out.println("Hello World"); // This is a single line that starts with the two forward slashes, 
                                         // also for useful for brief explanation of a code
      
      System.out.println("Hello World"); /* This is a multi line comment which starts with a forward slash ( / )
                                          followed by an asterisk ( * ), and ends with an asterisk and a forward slash.
                                          This comment can span to several lines*/
                                          
      System.out.println("Hello World"); /** This is a Documentation comment, which starts with a forward slash ( / )
                                          followed by two asterisks ( * ), and ends with an asterisk and a forward slash.
                                          This comment can span to several lines usually used to generate JavaDoc documentation
                                          eg. @tag description
                                          */
   }
}